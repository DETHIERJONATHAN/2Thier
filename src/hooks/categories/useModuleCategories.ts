export const useModuleCategories = () => {
  const { api } = useAuthenticatedApi();
  const { currentOrganization } = useAuth();
  const [categories, setCategories] = useState<ModuleCategory[]>([]);
  const [modules, setModules] = useState<any[]>([]); // ‚úÖ Ajout des modules
  const [loading, setLoading] = useState(true);
  const [error, setError] = useState<string | null>(null);

  // ‚úÖ Convertir Categories ‚Üí DynamicSection (pour compatibilit√© avec ModulesAdminPage.tsx)
  const allSections: DynamicSection[] = categories.map(category => ({
    id: category.id,
    title: category.name,
    description: category.description || '',
    iconName: category.icon,
    iconColor: category.iconColor,
    order: category.order,
    active: category.active,
    organizationId: category.organizationId,
    createdAt: category.createdAt,
    updatedAt: category.updatedAt
  }));

  // Initialiser les Categories par d√©faut
  const initializeDefaultCategories = useCallback(async () => {
    if (!currentOrganization?.id || !api) return;

    try {
      console.log(`üì¶ [useModuleCategories] Cr√©ation de ${defaultCategories.length} Categories par d√©faut...`);

      const createdCategories: ModuleCategory[] = [];

      // Cr√©er chaque category individuellement pour √©viter les erreurs
      for (const categoryData of defaultCategories) {
        try {
          const response = await api.post('/admin-modules/categories', {
            ...categoryData,
            organizationId: currentOrganization.id
          });

          if (response?.success && response.data) {
            createdCategories.push(response.data);
            console.log(`‚úÖ Category "${categoryData.name}" cr√©√©e`);
          }
        } catch (error) {
          console.warn(`‚ö†Ô∏è Erreur cr√©ation category "${categoryData.name}":`, error);
        }
      }

      if (createdCategories.length > 0) {
        console.log(`‚úÖ [useModuleCategories] ${createdCategories.length} Categories cr√©√©es avec succ√®s`);
        setCategories(createdCategories.sort((a, b) => a.order - b.order));
        message.success(`${createdCategories.length} Categories initialis√©es avec succ√®s`);
      }
    } catch (error) {
      console.error('‚ùå [useModuleCategories] Erreur lors de l\'initialisation des Categories:', error);
      setError('Erreur lors de l\'initialisation des categories');
    }
  }, [currentOrganization?.id, api]);

  // Charger les Categories depuis Prisma
  const loadCategories = useCallback(async () => {
    if (!currentOrganization?.id || !api) {
      setLoading(false);
      return;
    }

    try {
      setLoading(true);
      setError(null);
      console.log('üîÑ [useModuleCategories] Chargement Categories depuis Prisma...');
      
      // ‚úÖ Utiliser les routes admin-modules/categories existantes
      const response = await api.get(`/admin-modules/categories?organizationId=${currentOrganization.id}`);
      
      if (response?.success && Array.isArray(response.data) && response.data.length > 0) {
        console.log(`‚úÖ [useModuleCategories] ${response.data.length} Categories trouv√©es dans Prisma`);
        console.log(`‚úÖ Categories: ${response.data.map((c: ModuleCategory) => c.name).join(', ')}`);
        
        setCategories(response.data.sort((a: ModuleCategory, b: ModuleCategory) => a.order - b.order));
      } else {
        console.log('üìù [useModuleCategories] Aucune Category trouv√©e, cr√©ation des categories par d√©faut...');
        await initializeDefaultCategories();
      }
    } catch (error) {
      console.error('‚ùå [useModuleCategories] Erreur lors du chargement des Categories:', error);
      setError('Erreur lors du chargement des categories');
      
      // Fallback : cr√©er les categories par d√©faut
      console.log('‚ö†Ô∏è [useModuleCategories] Tentative de cr√©ation des categories par d√©faut');
      await initializeDefaultCategories();
    } finally {
      setLoading(false);
    }
  }, [currentOrganization?.id, api, initializeDefaultCategories]);

  // ‚úÖ Charger les modules (compatible avec ModulesAdminPage.tsx)
  const loadModules = useCallback(async () => {
    if (!currentOrganization?.id || !api) {
      console.warn('Pas d\'organisation courante, impossible de charger les modules');
      return;
    }

    try {
      console.log('üîÑ [useModuleCategories] Chargement des modules...');
      const response = await api.get(`/admin-modules?organizationId=${currentOrganization.id}`);
      
      if (response?.success && response.data?.sections) {
        // Extraire tous les modules de toutes les sections
        const allModules = response.data.sections.flatMap((section: any) => 
          section.modules || []
        );
        
        console.log(`‚úÖ [useModuleCategories] ${allModules.length} modules charg√©s`);
        setModules(allModules);
      }
    } catch (error) {
      console.error('‚ùå [useModuleCategories] Erreur lors du chargement des modules:', error);
    }
  }, [currentOrganization?.id, api]);

  // Activer/d√©sactiver une Category
  const toggleSectionActive = useCallback(async (categoryId: string) => {
    if (!api) return;

    try {
      console.log(`üîÑ [useModuleCategories] Toggle Category ${categoryId}...`);
      const category = categories.find(c => c.id === categoryId);
      if (!category) {
        console.error(`‚ùå [useModuleCategories] Category ${categoryId} introuvable`);
        return;
      }

      const newActiveStatus = !category.active;
      const response = await api.put(`/admin-modules/categories/${categoryId}`, {
        active: newActiveStatus
      });

      if (response?.success) {
        console.log(`‚úÖ [useModuleCategories] Category ${categoryId} ${newActiveStatus ? 'activ√©e' : 'd√©sactiv√©e'}`);
        setCategories(prev => prev.map(c => 
          c.id === categoryId 
            ? { ...c, active: newActiveStatus }
            : c
        ));
        message.success(`Category ${newActiveStatus ? 'activ√©e' : 'd√©sactiv√©e'} avec succ√®s`);
      }
    } catch (error) {
      console.error('‚ùå [useModuleCategories] Erreur lors du toggle Category:', error);
      message.error('Erreur lors de la mise √† jour de la category');
    }
  }, [categories, api]);

  // Supprimer une Category
  const deleteSection = useCallback(async (categoryId: string) => {
    if (!api) return;

    try {
      console.log(`üóëÔ∏è [useModuleCategories] Suppression Category ${categoryId}...`);
      const response = await api.delete(`/admin-modules/categories/${categoryId}`);
      
      if (response?.success) {
        console.log(`‚úÖ [useModuleCategories] Category ${categoryId} supprim√©e`);
        setCategories(prev => prev.filter(c => c.id !== categoryId));
        message.success('Category supprim√©e avec succ√®s');
      }
    } catch (error) {
      console.error('‚ùå [useModuleCategories] Erreur lors de la suppression Category:', error);
      message.error('Erreur lors de la suppression de la category');
    }
  }, [api]);

  // Ajouter une nouvelle Category
  const addSection = useCallback(async (name: string) => {
    try {
      const result = await api.post('/admin-modules/categories', { 
        name,
        description: `Category ${name}`,
        icon: 'AppstoreOutlined',
        iconColor: '#1890ff',
        order: categories.length + 1,
        active: true,
        organizationId: currentOrganization?.id,
        superAdminOnly: false
      });
      if (result?.success) {
        await loadCategories(); // Recharger les categories
        message.success('Category ajout√©e avec succ√®s');
      }
    } catch (error) {
      message.error('Erreur lors de l\'ajout de la category');
      console.error(error);
    }
  }, [api, loadCategories, categories.length, currentOrganization?.id]);

  // Mettre √† jour le nom d'une Category
  const updateSectionName = useCallback(async (categoryId: string, newName: string) => {
    if (!api) return;

    try {
      const response = await api.put(`/admin-modules/categories/${categoryId}`, {
        name: newName
      });

      if (response?.success) {
        setCategories(prev => prev.map(c => 
          c.id === categoryId 
            ? { ...c, name: newName }
            : c
        ));
        message.success('Nom de la category mis √† jour');
      }
    } catch (error) {
      console.error('Erreur lors de la mise √† jour du nom:', error);
      message.error('Erreur lors de la mise √† jour du nom');
    }
  }, [api]);

  // R√©organiser les Categories (drag & drop)
  const reorderSections = useCallback(async (reorderedSections: DynamicSection[]) => {
    // Mettre √† jour l'ordre localement d'abord
    const reorderedCategories = reorderedSections.map((section, index) => {
      const category = categories.find(c => c.id === section.id);
      return category ? { ...category, order: index + 1 } : null;
    }).filter(Boolean) as ModuleCategory[];
    
    setCategories(reorderedCategories);

    // Puis sauvegarder en BDD
    try {
      for (const [index, section] of reorderedSections.entries()) {
        await api.put(`/admin-modules/categories/${section.id}`, {
          order: index + 1
        });
      }
      console.log('‚úÖ Ordre des categories sauvegard√©');
    } catch (error) {
      console.error('Erreur lors de la sauvegarde de l\'ordre:', error);
      // Recharger en cas d'erreur
      loadCategories();
    }
  }, [categories, api, loadCategories]);

  // Charger au montage et quand l'organisation change
  useEffect(() => {
    if (currentOrganization?.id) {
      loadCategories();
      loadModules(); // ‚úÖ Charger aussi les modules
    }
  }, [loadCategories, loadModules, currentOrganization?.id]);

  // ‚úÖ Interface compatible avec ModulesAdminPage.tsx
  return {
    // Interface compatible
    allSections, // Categories converties en DynamicSection
    modules, // ‚úÖ Modules charg√©s
    setModules, // ‚úÖ Setter pour les modules
    loading,
    error,
    
    // Fonctions compatibles
    loadSections: loadCategories,
    loadModules, // ‚úÖ Fonction pour charger les modules
    toggleSectionActive,
    deleteSection,
    addSection,
    updateSectionName,
    reorderSections,
    
    // Donn√©es sp√©cifiques aux Categories
    categories,
    loadCategories
  };
};
